<template>
  <div id="register" class="page">
    <b-card class="login-box">
      <form @submit.prevent="register">
        <h1>Create Account</h1>
        <div role="alert" v-if="registrationErrors" class="alert alert-danger">
          {{ registrationErrorMsg }}
        </div>
        <b-form-group label="Username" label-for="username" :invalid-feedback="invalidFeedback">
          <b-form-input id="username" v-model="user.username" required autofocus />
        </b-form-group>
        <b-form-group label="Password" label-for="password" :invalid-feedback="invalidFeedback">
          <b-form-input id="password" type="password" v-model="user.password" required />
        </b-form-group>
        <b-form-group label="Confirm Password" label-for="confirmPassword" :invalid-feedback="invalidFeedback">
          <b-form-input id="confirmPassword" type="password" v-model="user.confirmPassword" required />
        </b-form-group>
        <b-button type="submit" variant="dark">Create Account</b-button>
        <p><router-link :to="{ name: 'login' }">Already have an account? Log in.</router-link></p>
      </form>
    </b-card>
  </div>
</template>

<script>
import authService from '../services/AuthService';

export default {
  name: 'register',
  data() {
    return {
      user: {
        username: '',
        password: '',
        confirmPassword: '',
        role: 'user',
      },
      registrationErrors: false,
      registrationErrorMsg: 'There were problems registering this user.',
    };
  },
  methods: {
    register() {
      if (this.user.password != this.user.confirmPassword) {
        this.registrationErrors = true;
        this.registrationErrorMsg = 'Password & Confirm Password do not match.';
      } else {
        authService
          .register(this.user)
          .then((response) => {
            if (response.status == 201) {
              this.$router.push({
                path: '/login',
                query: { registration: 'success' },
              });
            }
          })
          .catch((error) => {
            const response = error.response;
            this.registrationErrors = true;
            if (response.status === 400) {
              this.registrationErrorMsg = 'Bad Request: Validation Errors';
            }
          });
      }
    },
    clearErrors() {
      this.registrationErrors = false;
      this.registrationErrorMsg = 'There were problems registering this user.';
    },
  },
};
</script>

